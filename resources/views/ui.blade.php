                                <h3>UI/UX DESIGNER</h3>
                            </div>
                            <ul>
                                <h2>1. Pemahaman Dasar UI/UX</h2>
                                <li><strong>Apa itu UI dan UX?</strong>
                                    <ul>
                                        <li><strong>UI (User Interface):</strong> Fokus pada tampilan visual dan interaksi pengguna dengan elemen-elemen di aplikasi atau situs web.</li>
                                        <li><strong>UX (User Experience):</strong> Fokus pada pengalaman pengguna saat menggunakan produk, mencakup kepuasan, kemudahan, dan efisiensi.</li>
                                    </ul>
                                </li>
                                <li><strong>Prinsip Desain UI/UX:</strong>
                                    <ul>
                                        <li>Kesesuaian dengan pengguna (User-Centered Design)</li>
                                        <li>Konsistensi dan standar</li>
                                        <li>Navigasi yang jelas dan intuitif</li>
                                    </ul>
                                </li>
                            </ul>

                            <h2>2. Riset Pengguna</h2>
                            <ul>
                                <li><strong>Pengumpulan Data:</strong>
                                    <ul>
                                        <li>Wawancara pengguna</li>
                                        <li>Survei online</li>
                                        <li>Studi perilaku pengguna</li>
                                    </ul>
                                </li>
                                <li><strong>Persona Pengguna:</strong> Membuat representasi fiktif dari pengguna ideal untuk memahami kebutuhan mereka.</li>
                                <li><strong>User Journey Mapping:</strong> Melacak pengalaman pengguna dari awal hingga akhir saat menggunakan produk.</li>
                            </ul>

                            <h2>3. Wireframing dan Prototyping</h2>
                            <ul>
                                <li><strong>Wireframe:</strong> Kerangka sederhana yang menggambarkan tata letak elemen di halaman. Tools populer: Figma, Adobe XD, atau Balsamiq.</li>
                                <li><strong>Prototype:</strong> Versi interaktif dari wireframe yang memungkinkan simulasi pengalaman pengguna.</li>
                            </ul>

                            <h2>4. Prinsip Desain Visual</h2>
                            <ul>
                                <li><strong>Dasar-Dasar Desain:</strong>
                                    <ul>
                                        <li>Teori warna: Memilih palet warna yang sesuai.</li>
                                        <li>Tipografi: Memilih font yang mudah dibaca.</li>
                                        <li>Grid dan Spacing: Membantu menciptakan tata letak yang rapi.</li>
                                    </ul>
                                </li>
                                <li><strong>Desain Responsif:</strong> Membuat antarmuka yang bisa digunakan di berbagai perangkat (desktop, tablet, mobile).</li>
                            </ul>

                            <h2>5. Pengujian Pengguna</h2>
                            <ul>
                                <li><strong>Metode Pengujian:</strong>
                                    <ul>
                                        <li>Tes A/B: Membandingkan dua versi desain.</li>
                                        <li>Heuristic Evaluation: Memeriksa desain berdasarkan prinsip usability.</li>
                                    </ul>
                                </li>
                                <li><strong>Tools Pengujian:</strong> Maze, UsabilityHub, atau Hotjar.</li>
                                <li><strong>Iterasi:</strong> Perbaikan desain berdasarkan hasil pengujian.</li>
                            </ul>

                            <h2>6. Kolaborasi dengan Tim</h2>
                            <ul>
                                <li><strong>Komunikasi dengan Developer:</strong> Membuat desain yang dapat diimplementasikan secara teknis. Menyediakan style guide atau design system.</li>
                                <li><strong>Proyek Tim:</strong> Bekerja bersama product manager, developer, dan stakeholder untuk menyelaraskan visi.</li>
                            </ul>

                            <h2>7. Meningkatkan Kemampuan</h2>
                            <ul>
                                <li><strong>Belajar Alat Desain:</strong> Figma, Adobe XD, Sketch, atau Canva untuk pemula.</li>
                                <li><strong>Ikuti Tren Desain:</strong> Membaca artikel atau mengikuti komunitas seperti Dribbble, Behance, atau Medium.</li>
                                <li><strong>Proyek Portofolio:</strong> Buat desain UI/UX untuk aplikasi atau situs web fiktif sebagai bahan portofolio.</li>
                            </ul>

